public class Main {

    public static void main(String[] args) {
        System.out.println("Praca w grupie");

        for (int i = 0; i <= 100; i++) {
            System.out.println("Wartość iteratora to " + i);

        }
        System.out.println();

        for (int i = 100; i >= 0; --i) {
            System.out.println("Wartość iteratora to " + i);
        }

        System.out.println("cośtam");
    }
}
